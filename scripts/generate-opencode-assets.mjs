#!/usr/bin/env node
import crypto from 'node:crypto';
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const generatedBy = 'generated by scripts/generate-opencode-assets.mjs';
const opencodeDir = path.join(root, '.opencode');
const skillsOut = path.join(opencodeDir, 'skills');
const agentsOut = path.join(opencodeDir, 'agent');
const mcpInventoryOut = path.join(opencodeDir, 'MCP.md');
const profileReadmeOut = path.join(opencodeDir, 'profile', 'README.md');
const configPath = path.join(root, 'opencode.json');

function rel(file) {
  return path.relative(root, file).replaceAll(path.sep, '/');
}

function read(file) {
  return fs.readFileSync(file, 'utf8');
}

function write(file, text) {
  fs.mkdirSync(path.dirname(file), { recursive: true });
  fs.writeFileSync(file, text, 'utf8');
}

function walk(dir, predicate, out = []) {
  if (!fs.existsSync(dir)) return out;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.name === '.git' || entry.name === 'node_modules') continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, predicate, out);
    else if (!predicate || predicate(full)) out.push(full);
  }
  return out;
}

function slug(value) {
  return value
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/ae/g, 'ae')
    .replace(/oe/g, 'oe')
    .replace(/ue/g, 'ue')
    .replace(/ss/g, 'ss')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-{2,}/g, '-') || 'unnamed';
}

function hash(input, length = 8) {
  return crypto.createHash('sha1').update(input).digest('hex').slice(0, length);
}

function opencodeName(parts) {
  const base = slug(parts.filter(Boolean).join('-'));
  if (base.length <= 64) return base;
  return `${base.slice(0, 55).replace(/-+$/g, '')}-${hash(base)}`;
}

function splitFrontmatter(file) {
  const text = read(file);
  const match = text.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
  if (!match) throw new Error(`${rel(file)}: missing YAML frontmatter`);
  return { frontmatter: match[1], body: text.slice(match[0].length) };
}

function scalar(frontmatter, key) {
  const lines = frontmatter.split(/\r?\n/);
  for (let i = 0; i < lines.length; i += 1) {
    const m = lines[i].match(new RegExp(`^${key}\\s*:\\s*(.*)$`));
    if (!m) continue;
    const raw = m[1].trim();
    if (raw === '>' || raw === '|') {
      const parts = [];
      for (let j = i + 1; j < lines.length; j += 1) {
        if (/^[A-Za-z][\w-]*\s*:/.test(lines[j])) break;
        if (lines[j].trim()) parts.push(lines[j].trim());
      }
      return parts.join(raw === '>' ? ' ' : '\n').trim();
    }
    return raw.replace(/^['"]|['"]$/g, '').trim();
  }
  return '';
}

function yamlString(value) {
  return JSON.stringify(String(value));
}

function squashWhitespace(value) {
  return String(value || '').replace(/\s+/g, ' ').trim();
}

function truncate(value, maxLength) {
  const text = squashWhitespace(value);
  if (text.length <= maxLength) return text;
  return `${text.slice(0, maxLength - 3).replace(/\s+\S*$/g, '').replace(/[.,;:!?-]+$/g, '')}...`;
}

function pluginManifest(pluginRoot) {
  const manifestPath = path.join(pluginRoot, '.claude-plugin', 'plugin.json');
  if (!fs.existsSync(manifestPath)) return {};
  return JSON.parse(read(manifestPath));
}

function rewriteForOpencode(text, plugin) {
  let rewritten = text
    .replaceAll('~/.claude/plugins/config/claude-fuer-deutsches-recht/', './')
    .replaceAll('Claude Code', 'opencode')
    .replaceAll('Claude Desktop', 'opencode')
    .replaceAll('Claude', 'opencode');

  const pluginClaudeFile = `./${plugin}/CLAUDE.md`;
  const replacementProfile = `.opencode/profile/${plugin}.md`;
  rewritten = rewritten.replaceAll(pluginClaudeFile, replacementProfile);
  rewritten = rewritten.replace(/\.\/([a-z0-9-]+)\/CLAUDE\.md/g, (_match, referencedPlugin) => {
    return `.opencode/profile/${referencedPlugin}.md`;
  });
  rewritten = rewritten.replaceAll('~/.claude', '.opencode');
  return rewritten;
}

function resetGeneratedDir(dir) {
  const marker = path.join(dir, '.generated');
  if (fs.existsSync(dir)) {
    if (!fs.existsSync(marker) || !read(marker).includes(generatedBy)) {
      throw new Error(`${rel(dir)} exists but is not marked as generated; refusing to overwrite`);
    }
    fs.rmSync(dir, { recursive: true, force: true });
  }
  fs.mkdirSync(dir, { recursive: true });
  write(marker, `${generatedBy}\nDo not edit files in this directory manually.\n`);
}

function pluginRoots() {
  const marketplacePath = path.join(root, '.claude-plugin', 'marketplace.json');
  if (fs.existsSync(marketplacePath)) {
    const marketplace = JSON.parse(read(marketplacePath));
    return marketplace.plugins
      .map((plugin) => path.resolve(root, plugin.source || plugin.name))
      .filter((pluginRoot) => fs.existsSync(path.join(pluginRoot, '.claude-plugin', 'plugin.json')));
  }
  return walk(root, (file) => file.endsWith(path.join('.claude-plugin', 'plugin.json')))
    .map((file) => path.dirname(path.dirname(file)))
    .filter((pluginRoot) => path.basename(pluginRoot) !== '.claude-plugin')
    .sort();
}

function generateSkills(plugins) {
  resetGeneratedDir(skillsOut);
  const used = new Map();
  let count = 0;

  for (const pluginRoot of plugins) {
    const plugin = path.basename(pluginRoot);
    const skills = walk(path.join(pluginRoot, 'skills'), (file) => path.basename(file) === 'SKILL.md').sort();
    if (skills.length === 0) continue;

    const manifest = pluginManifest(pluginRoot);
    const manifestDescription = rewriteForOpencode(squashWhitespace(manifest.description), plugin);
    const description = truncate(`${plugin}: ${manifestDescription || `Workflows aus dem Plugin ${plugin}.`} Use this opencode router for ${plugin} requests; it selects source skills through skills-index/${plugin}.md and then reads the matching SKILL.md files.`, 900);
    let name = opencodeName([manifest.name || plugin]);
    const previous = used.get(name);
    if (previous && previous !== rel(pluginRoot)) name = opencodeName([manifest.name || plugin, hash(rel(pluginRoot), 6)]);
    used.set(name, rel(pluginRoot));

    const indexFile = path.join(root, 'skills-index', `${plugin}.md`);
    const indexInstruction = fs.existsSync(indexFile)
      ? `1. Read \`${rel(indexFile)}\` first. It is the compact catalog for the source skills in this plugin.`
      : `1. No \`skills-index/${plugin}.md\` file exists. Inspect \`${rel(path.join(pluginRoot, 'skills'))}/**/SKILL.md\` and choose by frontmatter description.`;
    const outDir = path.join(skillsOut, name);
    const outFile = path.join(outDir, 'SKILL.md');
    write(outFile, `---\nname: ${name}\ndescription: ${yamlString(description)}\n---\n\n# ${plugin} opencode skill router\n\n> Generated router for the source plugin \`${rel(pluginRoot)}\`. It keeps opencode's visible skill catalog small; it is not a replacement for the source skill bodies.\n\n## Source plugin\n\n- Manifest: \`${rel(path.join(pluginRoot, '.claude-plugin', 'plugin.json'))}\`\n- Skill catalog: \`skills-index/${plugin}.md\`\n- Source skill root: \`${rel(path.join(pluginRoot, 'skills'))}/\`\n- Source skills: ${skills.length}\n\nPlugin description: ${manifestDescription || 'Keine Plugin-Beschreibung im Manifest.'}\n\n## Routing workflow\n\n${indexInstruction}\n2. Match the user request to the smallest useful set of source skills. Prefer specific source skills over broad entry skills when the task is clear.\n3. Read every selected source file under \`${rel(path.join(pluginRoot, 'skills'))}/<skill-name>/SKILL.md\`.\n4. Treat the selected source skill body as the operative prompt for the task. Do not answer from this router alone.\n5. Keep the repository instructions from \`AGENTS.md\`, \`references/zitierweise.md\` and \`references/methodik-buergerliches-recht.md\` in force.\n6. If the user names a concrete source skill, read that source \`SKILL.md\` directly before working.\n`);
    count += 1;
  }

  return count;
}

function generateAgents() {
  resetGeneratedDir(agentsOut);
  const agentFiles = walk(root, (file) => file.includes(`${path.sep}agents${path.sep}`) && file.endsWith('.md'))
    .filter((file) => !rel(file).startsWith('.opencode/'))
    .sort();
  let count = 0;

  for (const agentFile of agentFiles) {
    const plugin = rel(agentFile).split('/')[0];
    const { frontmatter, body } = splitFrontmatter(agentFile);
    const sourceName = scalar(frontmatter, 'name') || path.basename(agentFile, '.md');
    const description = rewriteForOpencode(scalar(frontmatter, 'description') || `Subagent aus ${rel(agentFile)}`, plugin);
    const name = opencodeName([plugin, sourceName]);
    const rewritten = rewriteForOpencode(body, plugin);

    write(path.join(agentsOut, `${name}.md`), `---\nname: ${name}\ndescription: ${yamlString(description)}\nmode: subagent\npermission:\n  edit: ask\n  bash: ask\n  webfetch: ask\n---\n\n> Opencode-Port von \`${rel(agentFile)}\`. Claude-spezifische Tool- und Kurzmodellangaben wurden entfernt; externe MCP-Server muessen in \`opencode.json\` bewusst aktiviert werden.\n\n${rewritten.replace(/^\s+/, '')}`);
    count += 1;
  }

  return count;
}

function collectMcp() {
  const byUrl = new Map();
  const byName = new Map();
  const mcpFiles = walk(root, (file) => path.basename(file) === '.mcp.json')
    .filter((file) => !rel(file).startsWith('.opencode/'))
    .sort();

  for (const file of mcpFiles) {
    const source = rel(file).split('/')[0];
    const data = JSON.parse(read(file));
    for (const [rawName, server] of Object.entries(data.mcpServers || {})) {
      if (!server || typeof server.url !== 'string') continue;
      const url = server.url;
      let entry = byUrl.get(url);
      if (!entry) {
        let name = slug(server.title || rawName);
        const existingUrl = byName.get(name);
        if (existingUrl && existingUrl !== url) name = `${name}-${hash(url, 6)}`;
        byName.set(name, url);
        entry = { name, url, sources: new Set(), title: server.title || rawName };
        byUrl.set(url, entry);
      }
      entry.sources.add(source);
    }
  }

  return [...byUrl.values()].sort((a, b) => a.name.localeCompare(b.name));
}

function updateConfig(mcpEntries) {
  const existing = fs.existsSync(configPath) ? JSON.parse(read(configPath)) : {};
  const instructions = new Set(Array.isArray(existing.instructions) ? existing.instructions : []);
  for (const instruction of ['AGENTS.md', 'references/zitierweise.md', 'references/methodik-buergerliches-recht.md']) instructions.add(instruction);

  const skillPaths = new Set(Array.isArray(existing.skills?.paths) ? existing.skills.paths : []);
  skillPaths.add('.opencode/skills');

  const mcp = {};
  for (const entry of mcpEntries) {
    mcp[entry.name] = {
      type: 'remote',
      url: entry.url,
      enabled: false,
    };
  }

  const config = {
    ...existing,
    $schema: 'https://opencode.ai/config.json',
    instructions: [...instructions],
    skills: {
      ...(existing.skills || {}),
      paths: [...skillPaths],
    },
    permission: existing.permission || {
      edit: 'ask',
      bash: { '*': 'ask' },
      webfetch: 'ask',
      websearch: 'ask',
      skill: 'ask',
    },
    mcp,
  };

  write(configPath, `${JSON.stringify(config, null, 2)}\n`);
}

function writeMcpInventory(entries) {
  const rows = entries.map((entry) => `| \`${entry.name}\` | ${entry.url} | ${[...entry.sources].sort().join(', ')} |`);
  write(mcpInventoryOut, `# Opencode MCP Inventory\n\n${generatedBy}\n\nAll servers below are written to \`opencode.json\` with \`enabled: false\`. Enable only after checking provider contracts, confidentiality, data protection and mandate-specific approvals.\n\n| opencode name | URL | Source plugins |\n| --- | --- | --- |\n${rows.join('\n')}\n`);
}

function writeProfileReadme() {
  write(profileReadmeOut, `# Opencode Profiles\n\n${generatedBy}\n\nFormer Claude profile paths like \`~/.claude/plugins/config/claude-fuer-deutsches-recht/<plugin>/CLAUDE.md\` are mapped here as \`.opencode/profile/<plugin>.md\`.\n\nUse these files for local Kanzlei-, Team- or mandate-specific configuration. Do not put secrets, API keys or real mandate data into committed profile files.\n`);
}

const plugins = pluginRoots();
const skillCount = generateSkills(plugins);
const agentCount = generateAgents();
const mcpEntries = collectMcp();
updateConfig(mcpEntries);
writeMcpInventory(mcpEntries);
writeProfileReadme();

console.log(`Generated ${skillCount} opencode skill routers, ${agentCount} agents and ${mcpEntries.length} MCP server entries.`);
