        "(c) <b>Eigenes Markenpräsentationssystem</b>, in dem die K°°-Marke der Antragstellerin "
        "als Filter-Tag verfügbar ist, ohne dass die Antragstellerin dem zugestimmt hat; "
        "(d) <b>Eigene Werbung</b> mit Drittprodukten in Donauzon-Newslettern und auf der "
